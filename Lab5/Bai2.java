    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (products.stream().anyMatch(sp -> sp.getMaSanPham().equals(idField.getText()))) {
            JOptionPane.showMessageDialog(this, "ID đã tồn tại, vui lòng thử lại.");
            return;
        }

        cursorSp.execute("INSERT INTO SanPham VALUES (?,?,?,?,?);",
                idField.getText(), nameSPField.getText(), priceField.getText(), quantityField.getText(),
                productTypeField.getSelectedIndex()+1);
        products = getAllData();
        Actions.fillToTable(tableSP, products.stream()
                .map(p -> new Object[]{p.getMaSanPham(), p.getTenSP(), p.getLoaiSanPham().getTenLoai()})
                .collect(Collectors.toList()));
        deleteBtn.setEnabled(false);
        updateBtn.setEnabled(false);
    }//GEN-LAST:event_addBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        Object[][] data = Arrays.stream(tableSP.getSelectedRows())
                .mapToObj(i -> new Object[]{products.get(i).getMaSanPham()})
                .toArray(Object[][]::new);

        cursorSp.executeMany("DELETE FROM SanPham WHERE maSanPham = ?",
                data);
        products = getAllData();
        Actions.fillToTable(tableSP, products.stream()
                .map(p -> new Object[]{p.getMaSanPham(), p.getTenSP(), p.getLoaiSanPham().getTenLoai()})
                .collect(Collectors.toList()));
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        cursorSp.execute("""
                            UPDATE SanPham\s
                            SET tenSP = ?, donGia = ?, soLuong = ?, MALOAI = ?
                            WHERE maSanPham = ?;""",
                nameSPField.getText(), priceField.getText(), quantityField.getText(),
                productTypeField.getSelectedIndex()+1, idField.getText());
        SanPham product = products.get(tableSP.getSelectedRow());

        product.setTenSP(nameSPField.getText());
        product.setDonGia(new BigDecimal(priceField.getText()));
        product.setSoLuong(Integer.parseInt(quantityField.getText()));
        product.setLoaiSanPham(productTypes.get(productTypeField.getSelectedIndex()));
//            products = getAllData();
        Actions.fillToTable(tableSP, products.stream()
                .map(p -> new Object[]{
                        p.getMaSanPham(), p.getTenSP(), p.getLoaiSanPham().getTenLoai()})
                .collect(Collectors.toList()));
    }//GEN-LAST:event_updateBtnActionPerformed

