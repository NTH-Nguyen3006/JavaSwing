    private List getAllData() {
        return cursorSp.execute("""
                SELECT maSanPham, tenSP, MaLoai FROM SanPham;
                """).fetchAll().stream().map(
            sp -> {
                SanPham s = (SanPham) sp;
                LoaiSanPham proType = productTypes.stream().filter(
                                pt -> pt.getMaLoaiSP() == s.loaiSanPham.getMaLoaiSP())
                        .findFirst().orElse(null);
                if (proType != null)
                    s.getLoaiSanPham().setTenLoai(proType.getTenLoai());
                return s;
            }
        ).toList();
    }

