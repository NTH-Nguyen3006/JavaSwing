    private void Open_fileActionPerformed(java.awt.event.ActionEvent evt)  {//GEN-FIRST:event_Open_fileActionPerformed
        fileChooser.setFileFilter(new FileNameExtensionFilter(
                "Chá» chá»n file cÃ³ ÄuÃ´i (*.dat)", "dat"));

        if (fileChooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            filepath = fileChooser.getSelectedFile().getAbsolutePath();
            try (FileInputStream fip = new FileInputStream(filepath)) {
                String dataTextFile = new String(fip.readAllBytes(), "UTF-8");
                TextPreview.setText(decodeBase64(dataTextFile));
            } catch (IOException e) {
                throw new RuntimeException(e);
            }
        }
    }//GEN-LAST:event_Open_fileActionPerformed

    private void Save_FileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Save_FileActionPerformed
        if (!filepath.isEmpty() && isUpdateFile) {
            try {
                FileOutputStream fos = new FileOutputStream(filepath);
                fos.write(TextPreview.getText().getBytes());
            } catch (IOException e) {
                throw new RuntimeException(e);
            }
        }
        else if (filepath.isEmpty()) {
            fileChooser.setFileFilter(new FileNameExtensionFilter(
                    "Chá» chá»n file cÃ³ ÄuÃ´i (*.dat)", "dat"));
            if (fileChooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                filepath = fileChooser.getSelectedFile().getAbsolutePath();
                if (!filepath.endsWith(".dat"))
                    filepath = filepath.strip() + ".dat";
                try (FileOutputStream outputStream = new FileOutputStream(filepath)) {
                    outputStream.write(encodeTxt(TextPreview.getText()).getBytes());
                } catch (IOException e) {
                    throw new RuntimeException(e);
                }
            }
        }
    }//GEN-LAST:event_Save_FileActionPerformed

